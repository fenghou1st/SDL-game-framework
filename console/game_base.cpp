//// GameBase //////////////////////////////////////////////////////////////////////////////////////////////////////////

GameBase::GameBase(const string & org, const string & app) : impl(new Impl(org, app)) {}


GameBase::~GameBase() {}


bool GameBase::is_ok() const { return impl->is_ok(); }


string GameBase::get_pref_path() const { return impl->get_pref_path(); }


//// GameBase::Impl ////////////////////////////////////////////////////////////////////////////////////////////////////

GameBase::Impl::Impl(const string & org, const string & app)
	: _ok(false)
{
	if (!_init_pref_path(org, app)) return;

	log::init(_pref_path);

	if (SDL_Init(SDL_INIT_EVERYTHING) != 0)
	{
		log::error("SDL_Init");
		return;
	}

	if ((IMG_Init(IMG_INIT_PNG) & IMG_INIT_PNG) != IMG_INIT_PNG)
	{
		log::error("IMG_Init");
		return;
	}

	if (TTF_Init() != 0)
	{
		log::error("TTF_Init");
		return;
	}

	_ok = true;
}


GameBase::Impl::~Impl()
{
	TTF_Quit();
	IMG_Quit();
	SDL_Quit();
}


bool GameBase::Impl::_init_pref_path(const string & org, const string & app)
{
	auto base_path = SDL_GetPrefPath(org.c_str(), app.c_str());
	if (base_path == nullptr) return false;

	_pref_path = base_path;
	SDL_free(base_path);

	if (!boost::filesystem::exists(_pref_path) && !boost::filesystem::create_directories(_pref_path))
		return false;

	return true;
}
